#square root of a number without using math module
num = int(input("Enter a number: "))
sqroot = num ** 0.5
print("Square root of", num, "is", sqroot)
#square root of a number using math module
import math
num = int(input("Enter a number: "))
sqroot = math.sqrt(num)
print("Square root of", num, "is", sqroot)

